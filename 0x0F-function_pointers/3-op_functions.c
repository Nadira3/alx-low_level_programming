#include "3-calc.h"
/**
 * op_add - returns the sum of a and b
 * @a: first int
 * @b: second int
 * Return: sum of a and b
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - returns the difference a and b
 * @a: first int
 * @b: second int
 * Return: diff btw a and b
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - returns the product of a and b
 * @a: first int
 * @b: second int
 * Return: product of a and b
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - returns the quotient of a and b division
 * @a: first int
 * @b: second int
 * Return: division quotient
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - returns the remainder of a and b division
 * @a: first int
 * @b: second int
 * Return: division remainder
 */
int op_mod(int a, int b)
{
	return (a % b);
}
